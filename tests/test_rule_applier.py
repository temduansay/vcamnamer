class TestGenerateRules:
    def test_empty_mappings(self):
        result = generate_rules({})
        assert _HEADER in result
        assert "SUBSYSTEM" not in result

    def test_single_mapping(self):
        result = generate_rules({"/dev/video0": "OBS Studio Camera"})
        assert 'KERNEL=="video0"' in result
        assert 'SYMLINK+="vcam/OBS_Studio_Camera"' in result
        assert "video4linux" in result

    def test_multiple_mappings_sorted(self):
        result = generate_rules(
            {
                "/dev/video2": "Secondary",
                "/dev/video0": "Primary",
            }
        )
        lines = [l for l in result.splitlines() if "SUBSYSTEM" in l]
        assert "video0" in lines[0]
        assert "video2" in lines[1]

    def test_header_always_present(self):
        result = generate_rules({"/dev/video0": "Cam"})
        assert "AUTO-GENERATED by vcamnamer" in result

    def test_special_name_slugified_in_rule(self):
        result = generate_rules({"/dev/video0": "My Cam!"})
        assert "SYMLINK" in result
        # exclamation mark becomes underscore then trailing underscore is stripped
        assert "My_Cam" in result

    def test_rule_format(self):
        result = generate_rules({"/dev/video0": "Cam One"})
        # Must contain the full udev rule structure
        assert 'SUBSYSTEM=="video4linux"' in result
        assert 'KERNEL=="video0"' in result
        assert 'SYMLINK+="vcam/Cam_One"' in result


