def test_write_rules_file_permission_error(monkeypatch, tmp_path):
    """_write_rules_file raises PermissionError with helpful message."""
    import vcamnamer.rule_applier as ra

    ro_dir = tmp_path / "ro"
    ro_dir.mkdir()
    monkeypatch.setattr(ra, "RULES_DIR", ro_dir)
    monkeypatch.setattr(ra, "RULES_FILE", ro_dir / "99-vcamnamer.rules")

    # Make the dir read-only
    ro_dir.chmod(0o555)
    try:
        with pytest.raises(PermissionError, match="Cannot write"):
            ra._write_rules_file("# test")
    finally:
        ro_dir.chmod(0o755)


