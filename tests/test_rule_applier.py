class TestSlugify:
    def test_simple_name(self):
        assert _slugify("OBS Camera") == "OBS_Camera"

    def test_special_chars_replaced(self):
        # trailing underscore from "!" is stripped
        assert _slugify("My Camera!") == "My_Camera"

    def test_spaces_replaced(self):
        assert _slugify("Presentation Cam") == "Presentation_Cam"

    def test_trailing_underscores_stripped(self):
        assert _slugify("  !!  ") == "vcam"

    def test_safe_chars_preserved(self):
        # Alphanumerics, hyphens, dots are preserved
        assert _slugify("Cam-1.2") == "Cam-1.2"

    def test_empty_string(self):
        assert _slugify("") == "vcam"


